public class ex2 {
    public static double perimetroRettangolo(double primoLato, double secondoLato) {
        return 2 * (primoLato + secondoLato);
    }

    public static int pariDispari(int num) {
        if (num % 2 == 0) {
            return 0;
        } else {
            return 1;
        }
    }
}
